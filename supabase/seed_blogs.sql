1. ALWAYS USE BOTH HANDS TO REMOVE YOUR GLASSES
Taking glasses off with one hand twists the frame gradually over time, causing it to become misaligned or weakening the hinges. Always use both hands, holding each temple (arm) as you remove them.

2. NEVER PLACE THEM FACE-DOWN
Resting your glasses with the lenses facing down is one of the most common causes of scratched lenses. Always place them face-up on a clean surface, or better yet, inside their case.

3. USE A MICROFIBRE CLOTH — NOT YOUR SHIRT
Cleaning lenses with your shirt, tissue, or paper towel can cause micro-scratches that build up over time. Always use the microfibre cloth that came with your glasses. For stubborn smudges, use lens cleaning spray or simply rinse under lukewarm water.

